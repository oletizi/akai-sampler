export function writeHeader(buf) {
    buf.set([
        0x52, 0x49, 0x46, 0x46, // 'RIFF'
        0x00, 0x00, 0x00, 0x00, //  0
        0x41, 0x50, 0x52, 0x47, // 'APRG'
        0x70, 0x72, 0x67, 0x20, // 'prg '
        0x06, 0x00, 0x00, 0x00, //  6
    ], 0)
}

export function writeManifest(buf, programNumber = 0, keygroupCount = 0x1) {
    buf.set([
        0x02, programNumber, keygroupCount, 0x00,
        0x02, 0x01,
        0x6f, 0x75, 0x74, 0x20, // 'out '
        0x08, 0x00, 0x00, 0x00
    ], 0x14)
}